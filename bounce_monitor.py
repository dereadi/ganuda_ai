#!/usr/bin/env python3
import json
from coinbase.rest import RESTClient
import time

config = json.load(open('/home/dereadi/.coinbase_config.json'))
key = config['api_key'].split('/')[-1]
client = RESTClient(api_key=key, api_secret=config['api_secret'], timeout=3)

print('üèÄ BOUNCE IN PROGRESS!')
print('=' * 50)

# Get multiple price checks
prices = []
for i in range(3):
    ticker = client.get_product('BTC-USD')
    price = float(ticker.price)
    prices.append(price)
    print(f'   Tick {i+1}: ${price:,.2f}')
    if i < 2:
        time.sleep(1)

# Calculate momentum
momentum = prices[-1] - prices[0]
avg_price = sum(prices) / len(prices)

print(f'\nüìä BOUNCE STATS:')
print(f'   Support Level: $117,056')
print(f'   Current Avg:   ${avg_price:,.2f}')
print(f'   3-sec Move:    ${momentum:+,.2f}')
print(f'   Above Support: ${avg_price - 117056:+,.2f}')

if momentum > 0:
    print('\nüöÄ BOUNCING UP!')
elif momentum < 0:
    print('\nüìâ Testing support...')
else:
    print('\n‚öñÔ∏è Consolidating...')

